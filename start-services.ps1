# Function to check if port is in use
function Test-Port {
    param([int]$Port)
    $connection = Get-NetTCPConnection -LocalPort $Port -ErrorAction SilentlyContinue
    return $null -ne $connection
}

# Function to kill process on port
function Stop-ProcessOnPort {
    param([int]$Port, [string]$ServiceName)
    
    if (Test-Port $Port) {
        Write-Host "âš ï¸  Stopping existing $ServiceName on port $Port..." -ForegroundColor Yellow
        $processes = Get-NetTCPConnection -LocalPort $Port -ErrorAction SilentlyContinue | 
                     Select-Object -ExpandProperty OwningProcess -Unique
        foreach ($pid in $processes) {
            Stop-Process -Id $pid -Force -ErrorAction SilentlyContinue
        }
        Start-Sleep -Seconds 2
    }
}

# Function to wait for service
function Wait-ForService {
    param([string]$Host, [int]$Port, [string]$ServiceName, [int]$MaxAttempts = 30)
    
    Write-Host "â³ Waiting for $ServiceName to be ready..." -ForegroundColor Blue
    $attempt = 0
    
    while ($attempt -lt $MaxAttempts) {
        try {
            $tcpClient = New-Object System.Net.Sockets.TcpClient
            $tcpClient.Connect($Host, $Port)
            $tcpClient.Close()
            Write-Host "âœ… $ServiceName is ready!" -ForegroundColor Green
            return $true
        }
        catch {
            $attempt++
            Start-Sleep -Seconds 1
        }
    }
    
    Write-Host "âŒ $ServiceName failed to start after $MaxAttempts seconds" -ForegroundColor Red
    return $false
}

